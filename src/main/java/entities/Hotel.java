package entities;

/**
 *
 * @author Jonas
 */

public class Hotel {
    
    String id;
    String name;
    String address;
    String phone;
    String content;
    String price;
    
    public Hotel(){
        
    }
    
    public Hotel(String id, String name, String address, String phone, String content, String price) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.content = content;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
    
}
